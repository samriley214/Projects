class Point {
    private final double X;
    private final double Y;
    private final double Z;
    Point(double x, double y, double z) {
        this.X = x;
        this.Y = y;
        this.Z = z;
    }
    double getX(){
        return this.X;
    }
    double getY(){
        return this.Y;
    }
    double getZ(){
        return this.Z;
    }

}
